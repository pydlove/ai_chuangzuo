wb = openpyxl.Workbook()
ws = wb.active
ws.title = "用户端测试用例"

# 写入表头
for col_idx, header in enumerate(HEADERS, 1):
    cell = ws.cell(row=1, column=col_idx, value=header)
    cell.font = Font(bold=True, color="FFFFFF")
    cell.fill = PatternFill("solid", fgColor="07C160")
    cell.alignment = Alignment(horizontal="center", vertical="center", wrap_text=True)

# 写入数据
for row_idx, case in enumerate(cases, 2):
    # 生成用例编号
    case_id = f"UC-{row_idx-1:04d}"
    ws.cell(row=row_idx, column=1, value=case_id)
    for col_idx, header in enumerate(HEADERS[1:], 2):
        value = case.get(header, "")
        cell = ws.cell(row=row_idx, column=col_idx, value=value)
        cell.alignment = Alignment(vertical="top", wrap_text=True)
        # 优先级颜色
        if header == "优先级":
            if value == "P0":
                cell.fill = PatternFill("solid", fgColor="FFCCCC")
            elif value == "P1":
                cell.fill = PatternFill("solid", fgColor="FFE699")
            elif value == "P2":
                cell.fill = PatternFill("solid", fgColor="CCE5FF")
        # 测试类型颜色
        if header == "测试类型":
            if "安全" in value or "异常" in value:
                cell.fill = PatternFill("solid", fgColor="E6E6E6")

# 设置列宽
col_widths = [10, 14, 18, 28, 22, 38, 22, 38, 8, 14, 10, 10, 24, 12, 12]
for i, width in enumerate(col_widths, 1):
    ws.column_dimensions[get_column_letter(i)].width = width

# 设置行高
ws.row_dimensions[1].height = 30
for row_idx in range(2, len(cases) + 2):
    ws.row_dimensions[row_idx].height = 60

# 边框
thin = Side(style="thin", color="DDDDDD")
for row in ws.iter_rows(min_row=1, max_row=len(cases)+1, min_col=1, max_col=len(HEADERS)):
    for cell in row:
        cell.border = Border(left=thin, right=thin, top=thin, bottom=thin)

# 冻结首行
ws.freeze_panes = "A2"

# 下拉选项（是否已执行、执行结果）
from openpyxl.worksheet.datavalidation import DataValidation

dv_exec = DataValidation(type="list", formula1='"Y,N"', allow_blank=True)
dv_exec.prompt = "请选择是否已执行"
ws.add_data_validation(dv_exec)
dv_exec.add(f"K2:K{len(cases)+1}")

dv_result = DataValidation(type="list", formula1='"Pass,Fail,Blocked,NA"', allow_blank=True)
dv_result.prompt = "请选择执行结果"
ws.add_data_validation(dv_result)
dv_result.add(f"L2:L{len(cases)+1}")

wb.save(OUTPUT_PATH)
print(f"已生成 {len(cases)} 条用户端测试用例：{OUTPUT_PATH}")
